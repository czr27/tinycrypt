void bin2hex(const char *s, void *bin, int len) {
    int  i;
    char c;
    uint8_t *b=bin;
    
    printf ("\n%s=", s);
    
    for (i=0; i<len; i++) {
      if ((i & 7)==0) putchar('\n');
      if (i==len-1) c='\n'; else c=',';
      printf ("0x%02x%c ", b[i], c);
    }
}

